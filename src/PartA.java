public class PartA {
    public static void main(String[] args) {
        // task one
        for(int count=0; count<=30; count++) {
            System.out.print(count + " ");
        }

        System.out.println();

        // task two
        for(int count=30; count>=0; count--){
            System.out.print(count + " ");
        }

        System.out.println();

        // task three
        for(int count=0; count<=18; count+=3){
            System.out.print(count + " ");
        }

        System.out.println();

        // task four
        for(int count=10; count>=0; count-=2){
            System.out.print(count + " ");
        }
    }
}
